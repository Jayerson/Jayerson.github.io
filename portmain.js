function showTopics() {
    var x = document.getElementById("collapsible");
    if (x.style.display === "block") {
      x.style.display = "none";
    } else {
      x.style.display = "block";
    }
  }

function showEng() {
    var x = document.getElementById("be-hidden");
    if (x.style.display === "block") {
      x.style.display = "none";
    } else {
      x.style.display = "block";
    }
}
